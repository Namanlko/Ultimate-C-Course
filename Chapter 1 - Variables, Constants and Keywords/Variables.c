// Variable: A variable is a name given to memory location which is used to store a value. Variable is an entity whose value can be changed.

#include<stdio.h>

int main(){
    
    int a = 3; // a is an integer variable which hold value 3
    float b = 4.3; // b is a floating point variable which hold value 4.3
    char c = 'A'; // c is a character variable which hold value 'A'

    printf("%d\n",a);
    printf("%f\n",b);
    printf("%c\n",c);

    return 0;
}
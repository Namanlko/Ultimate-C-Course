// Keywords: These are the reserved words, whose meaning is already known to the compiler. There are 32 keywords available in C.
// Example - auto, double, int, struct, break, long, else, switch, case, return etc.

#include<stdio.h>

int main(){

    int a = 5;
    float b = 3.4;
    char c = 'B';

    printf("%d %f %c",a,b,c);

    return 0;
}

// Here int, float, char, printf and return are keywords.